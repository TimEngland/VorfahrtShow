package pLFileCreater;

public class Auto {
	

	private String zufahrt;
	private String abfahrt;
	
	public Auto( String zufahrt, String abfahrt) {
		super();
		
		this.zufahrt = zufahrt;
		this.abfahrt = abfahrt;
	}
	
	
	public String getZufahrt() {
		return zufahrt;
	}
	public void setZufahrt(String zufahrt) {
		this.zufahrt = zufahrt;
	}


	public String getAbfahrt() {
		return abfahrt;
	}


	public void setAbfahrt(String abfahrt) {
		this.abfahrt = abfahrt;
	}
	
	
	
	
	

}
